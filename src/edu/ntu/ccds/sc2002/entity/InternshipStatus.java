package edu.ntu.ccds.sc2002.entity;

/**
 * Enumeration for internship status.
 */
public enum InternshipStatus {
    /**
     * Internship is pending review.
     */
    PENDING,
    /**
     * Internship is approved.
     */
    APPROVED,
    /**
     * Internship is rejected.
     */
    REJECTED,
    /**
     * Internship is filled.
     */
    FILLED
}